function [f] = Leg_1D_P15(x,P);
% 
% Leg_1D_P15  returns the value of the 1D Legendre polynomial of order P,
%             evaluated at point(s) x.
%
% Synopsis:  [f] = Leg_1D_P15(x,P);
%
% Inputs:    x = points abscissa
%            P = Legendre polynomial order
% Output:    f = Legendre polynomial values at the point
%
% Remark:    P=15 is the maximum order
%
[c,l] = size(x);

switch P
    case 0,
        f = ones(c,l);
    case 1,
        f = x;
    case 2,
        f = 0.5.*(3.*x.^2-1);
    case 3,
        f = 0.5.*(5.*x.^3-3.*x);
    case 4,
        f = 0.125.*(35.*x.^4 - 30.*x.^2 + 3);
    case 5,
        f = 0.125.*(63.*x.^5 - 70.*x.^3 + 15.*x);
    case 6,
        f = 0.0625.*(231.*x.^6 - 315.*x.^4 + 105.*x.^2 - 5);
    case 7,
        f = 0.0625.*(429.*x.^7 - 693.*x.^5 + 315.*x.^3 - 35.*x);
    case 8,
        f = (6435.*x.^8 - 12012.*x.^6 + 6930.*x.^4 -1260.*x.^2 + 35)./128;
    case 9,
        f = 12155/128.*x.^9 - 6435/32.*x.^7 + 9009/64.*x.^5 - 1155/32.*x.^3 + 315/128.*x;
    case 10,
        f = 46189/256.*x.^10 - 109395/256.*x.^8 + 45045/128.*x.^6 - 15015/128.*x.^4 +3465/256.*x.^2 - 63/256;
    case 11,
        f = 88179/256.*x.^11 - 230945/256.*x.^9 + 109395/128.*x.^7 - 45045/128.*x.^5 +15015/256.*x.^3 - 693/256.*x;
    case 12,
        f = 676039/1024.*x.^12 - 969969/512.*x.^10 + 2078505/1024.*x.^8 - 255255/256.*x.^6 + 225225/1024.*x.^4 - 9009/512.*x.^2 + 231/1024;
    case 13,
        f = 1300075/1024.*x.^13 - 2028117/512.*x.^11 + 4849845/1024.*x.^9 - 692835/256.*x.^7 + 765765/1024.*x.^5 - 45045/512.*x.^3 + 3003/1024.*x;
    case 14,
        f = 5014575/2048.*x.^14 - 16900975/2048.*x.^12 + 22309287/2048.*x.^10 - 14549535/2048.*x.^8 + 4849845/2048.*x.^6 - 765765/2048.*x.^4 + 45045/2048.*x.^2 - 429/2048;
    case 15,
        f = 9694845/2048.*x.^15 - 35102025/2048.*x.^13 + 50702925/2048.*x.^11 - 37182145/2048.*x.^9 + 14549535/2048.*x.^7 - 2909907/2048.*x.^5 +255255/2048.*x.^3 - 6435/2048.*x;
    otherwise,
        error('problem with Leg_1D_P15.m');
end

